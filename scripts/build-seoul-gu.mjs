// One-off build script: converts the Seoul gu GeoJSON into a compact TS data
// module with SVG path strings + label centroids fitted to a 1000x750 viewBox.
import fs from 'node:fs'

const W = 1000
const H = 750
const PAD = 16

const geo = JSON.parse(fs.readFileSync('/tmp/seoul.json', 'utf8'))

// Collect bounds
let minLon = Infinity
let maxLon = -Infinity
let minLat = Infinity
let maxLat = -Infinity

function eachRing(feature, fn) {
  const g = feature.geometry
  if (g.type === 'Polygon') {
    g.coordinates.forEach((ring) => fn(ring))
  } else if (g.type === 'MultiPolygon') {
    g.coordinates.forEach((poly) => poly.forEach((ring) => fn(ring)))
  }
}

geo.features.forEach((f) =>
  eachRing(f, (ring) =>
    ring.forEach(([lon, lat]) => {
      if (lon < minLon) minLon = lon
      if (lon > maxLon) maxLon = lon
      if (lat < minLat) minLat = lat
      if (lat > maxLat) maxLat = lat
    }),
  ),
)

// Simple equirectangular projection corrected for latitude, fit to viewBox.
const midLat = (minLat + maxLat) / 2
const lonScale = Math.cos((midLat * Math.PI) / 180)
const geoW = (maxLon - minLon) * lonScale
const geoH = maxLat - minLat
const scale = Math.min((W - PAD * 2) / geoW, (H - PAD * 2) / geoH)
const offsetX = (W - geoW * scale) / 2
const offsetY = (H - geoH * scale) / 2

function project(lon, lat) {
  const x = offsetX + (lon - minLon) * lonScale * scale
  // Flip Y (SVG y grows downward; latitude grows upward)
  const y = offsetY + (maxLat - lat) * scale
  return [x, y]
}

function ringToPath(ring) {
  return (
    ring
      .map(([lon, lat], i) => {
        const [x, y] = project(lon, lat)
        return `${i === 0 ? 'M' : 'L'}${x.toFixed(1)},${y.toFixed(1)}`
      })
      .join(' ') + 'Z'
  )
}

function featureToPath(feature) {
  const parts = []
  eachRing(feature, (ring) => parts.push(ringToPath(ring)))
  return parts.join(' ')
}

// Centroid of the largest ring, for label placement.
function featureCentroid(feature) {
  let best = null
  let bestArea = 0
  eachRing(feature, (ring) => {
    let area = 0
    let cx = 0
    let cy = 0
    for (let i = 0; i < ring.length - 1; i++) {
      const [x0, y0] = project(ring[i][0], ring[i][1])
      const [x1, y1] = project(ring[i + 1][0], ring[i + 1][1])
      const a = x0 * y1 - x1 * y0
      area += a
      cx += (x0 + x1) * a
      cy += (y0 + y1) * a
    }
    area /= 2
    if (Math.abs(area) > Math.abs(bestArea) && area !== 0) {
      bestArea = area
      best = [cx / (6 * area), cy / (6 * area)]
    }
  })

  if (best) return best

  // Fallback: average of all projected vertices.
  let sx = 0
  let sy = 0
  let n = 0
  eachRing(feature, (ring) =>
    ring.forEach(([lon, lat]) => {
      const [x, y] = project(lon, lat)
      sx += x
      sy += y
      n++
    }),
  )
  return [sx / n, sy / n]
}

const districts = geo.features.map((f) => {
  const [cx, cy] = featureCentroid(f)
  return {
    code: f.properties.code,
    name: f.properties.name,
    nameEng: f.properties.name_eng,
    path: featureToPath(f),
    cx: Math.round(cx),
    cy: Math.round(cy),
  }
})

const out = `// AUTO-GENERATED by scripts/build-seoul-gu.mjs from a Seoul gu GeoJSON.
// Do not edit by hand; re-run the script to regenerate.
export interface GuDistrict {
  code: string
  name: string
  nameEng: string
  path: string
  cx: number
  cy: number
}

export const GU_VIEWBOX = { width: ${W}, height: ${H} }

// Projection parameters so other modules (e.g. neighborhood pins) can map
// lon/lat to the same SVG coordinate space.
export const GU_PROJECTION = {
  minLon: ${minLon},
  maxLat: ${maxLat},
  lonScale: ${lonScale},
  scale: ${scale},
  offsetX: ${offsetX},
  offsetY: ${offsetY},
}

/** Project [lon, lat] into the GU_VIEWBOX coordinate space. */
export function projectLonLat(lon: number, lat: number): { x: number; y: number } {
  return {
    x: GU_PROJECTION.offsetX + (lon - GU_PROJECTION.minLon) * GU_PROJECTION.lonScale * GU_PROJECTION.scale,
    y: GU_PROJECTION.offsetY + (GU_PROJECTION.maxLat - lat) * GU_PROJECTION.scale,
  }
}

export const guDistricts: GuDistrict[] = ${JSON.stringify(districts, null, 2)}
`

fs.writeFileSync('src/data/seoulGu.ts', out)
console.log(`Wrote src/data/seoulGu.ts with ${districts.length} districts`)
